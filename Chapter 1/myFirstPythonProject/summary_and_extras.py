# The following line prints "Hello, World!" to the screen.
print("Hello, World!")

# Newline (\n): Inserts a line break.
print("Line 1\nLine 2")

# Tab (\t): Inserts a tab character.
print("First\tSecond")

# Carriage Return (\r): Moves the cursor to the beginning of the line.
print("Overwritten\rNew Text")

# Single Quote (') and Double Quote ("): To include single or double quotes within a string.
print("He said, 'Hello!'")

# Backslash (\\): To include a literal backslash within a string.
print("C:\\Users\\Username")
