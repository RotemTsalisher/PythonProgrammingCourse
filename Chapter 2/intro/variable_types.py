# Integer = a whole number
int_variable = 5

# Float = a floating point number (example: 1.25)
float_variable = 1.25

# String = a stream of char's wrapped with " or '
string_variable = "Python"
# string_variable = 'Python'

# Bool = a variable that holds the value "True" or "False"
bool_variable = True

print("int_variable =", int_variable)
print("float_variable =", float_variable)
print("string_variable =", string_variable)
print("bool_variable =", bool_variable)
